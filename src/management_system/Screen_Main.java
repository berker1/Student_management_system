    private void btn_lva_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_lva_saveActionPerformed
        LVA vl;
        vl = new LVA(tf_lva_name.getText(), tf_lva_code.getText(), tf_lva_kredit.getText());

        String[] lva = new String[3];
        lva[0] = tf_lva_name.getText();
        lva[1] = tf_lva_code.getText();
        lva[2] = tf_lva_kredit.getText();

        if (lva[1].isEmpty() == false) {
            if (LVA.kontrolCode(lva[1])) {
                if (uni.lvaExistiert(lva[1]) == false) {
                    DefaultTableModel lva_dtm = (DefaultTableModel) table_lvas.getModel();
                    lva_dtm.addRow(lva);;
                    uni.getLvas().add(vl);
                    info_lva_message.setText("LVA erfolgreich hinzugefgt");
                } else {
                    info_lva_message.setText("Information Nachricht");
                    JOptionPane.showMessageDialog(jPanel16, "LVA existiert schon");
                }
            } else {
                info_lva_message.setText("Information Nachricht");
                JOptionPane.showMessageDialog(jPanel16, "LVA ist nicht gltig", "Achtung!", JOptionPane.WARNING_MESSAGE);

            }
        } else {
            info_lva_message.setText("Information Nachricht");
            JOptionPane.showMessageDialog(jPanel16, "LVA Code kann nicht leer sein", "Achtung!", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btn_lva_saveActionPerformed

    private void btn_lva_aktualisierenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_lva_aktualisierenActionPerformed

        int i = table_lvas.getSelectedRow();
        if (i >= 0) {
            LVA lva2 = uni.getLvas().get(i);
            lva2.setLvName(tf_lva_name.getText());
            lva2.setLvKredit(tf_lva_kredit.getText());

            table_lvas.getModel().setValueAt(lva2.getLvName(), i, 0);
            table_lvas.getModel().setValueAt(lva2.getLvKredit(), i, 2);
            info_lva_message.setText("LVA Informationen sind aktualisiert");

            if (!tf_lva_code.getText().equals(uni.getLvas().get(i).getLvCode())) {
                info_lva_message.setText("LVA Code kann nicht geandert werden"
                        + ", Andere Informationen sind aktualisiert");
                tf_lva_code.setText(uni.getLvas().get(i).getLvCode());
            }

        } else {
            info_lva_message.setText("Information Nachricht");
            JOptionPane.showMessageDialog(jPanel16, "Es gibt keine ausgewhlte LVA fr die Aktion");
        }
    }//GEN-LAST:event_btn_lva_aktualisierenActionPerformed

