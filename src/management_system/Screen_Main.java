        ss2[0] = tf_s_name.getText();
        ss2[1] = tf_s_nachname.getText();
        ss2[2] = tf_s_nummer.getText();
        String[] ss = new String[10];
        ss[0] = tf_s_name.getText();
        ss[1] = tf_s_nachname.getText();
        ss[2] = tf_s_nummer.getText();
        ss[3] = tf_s_age.getText();
